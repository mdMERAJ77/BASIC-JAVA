public class Boolean {

    public static void main(String[] args) {
        String name="MERAJ";

        System.out.println(name.startsWith("ME"));  //OUTPUT true
        System.out.println(name.startsWith("RAJ")); //OUTPUT false
        System.out.println(name.endsWith("ME"));    //OUTPUT false
        System.out.println(name.endsWith("RAJ"));   //OUTPUT true
    }
}
