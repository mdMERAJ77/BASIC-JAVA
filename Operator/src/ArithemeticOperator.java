public class ArithemeticOperator {
    public static void main(String[] args) {
        int a=5;
        int b=2;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);// They give Quotient     Division

        System.out.println(a%b); // Modules Operator to get Remainder in integer division

        System.out.println("When use {double} DataTypes");
        double c=5;
        double d=2;
        System.out.println(c/d);
        System.out.println(c%d); // Note only use Modules Operator in integer DataTypes
    }
}
