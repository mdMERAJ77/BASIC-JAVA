public class BitwiseXorOperator {       // also known as Bitwise exclusive OR
    public static void main(String[] args) {

        int p=9, q=10;      // 9  Binary Number= 1 0 0 1
                            // 10 Binary Number= 1 0 1 0
        //  note:- Two Bit same then output false(0) otherwise true(1)

        System.out.println(p ^ q); // output = 3
    }
}
